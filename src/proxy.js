import { clerkMiddleware, createRouteMatcher } from "@clerk/nextjs/server";
import { NextResponse } from "next/server";

const isFreelancerPlans = createRouteMatcher([
  "/freelancer/plans",
  "/freelancer/plans/(.*)",
]);
const isProtectedFreelancer = createRouteMatcher(["/freelancer/(.*)"]);
const isClientRoute = createRouteMatcher(["/client/(.*)"]);

export default clerkMiddleware(async (auth, req) => {
  if (isFreelancerPlans(req)) return NextResponse.next();
  if (isProtectedFreelancer(req) || isClientRoute(req)) await auth.protect();
  return NextResponse.next();
});

export const config = {
  matcher: [
    "/((?!_next|[^?]*\\.(?:html?|css|js(?!on)|jpe?g|webp|png|gif|svg|ttf|woff2?|ico|csv|docx?|xlsx?|zip|webmanifest)).*)",
    "/(api|trpc)(.*)",
  ],
};
